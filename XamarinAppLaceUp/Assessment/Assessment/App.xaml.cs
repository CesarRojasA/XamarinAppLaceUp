using Assessment.ViewModels;
using Assessment.Views;
using Prism;
using Prism.Ioc;
using Xamarin.Essentials.Implementation;
using Xamarin.Essentials.Interfaces;
using Xamarin.Forms;

namespace Assessment
{
    public partial class App
    {
        public App(IPlatformInitializer initializer)
            : base(initializer)
        {
        }

        protected override async void OnInitialized()
        {
            InitializeComponent();

            await NavigationService.NavigateAsync("NavigationPage/ProductCatalogPage");
        }

        protected override void RegisterTypes(IContainerRegistry containerRegistry)
        {
            containerRegistry.RegisterSingleton<IAppInfo, AppInfoImplementation>();

            containerRegistry.RegisterForNavigation<NavigationPage>();
            containerRegistry.RegisterForNavigation<ProductCatalogPage, ProductCatalogViewModel>();
            containerRegistry.RegisterForNavigation<CheckoutPage, CheckoutViewModel>();
            containerRegistry.RegisterForNavigation<ProductDetailsPage, ProductDetailsViewModel>();
        }
    }
}
